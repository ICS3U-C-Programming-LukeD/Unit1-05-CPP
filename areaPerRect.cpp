// Calculates the area and perimeter of a rectangle

#include <cmath>
#include <iostream>
int main() {
    std::cout << "A rectangle with the" << std::endl;
    std::cout << "width of 10 and length of 6" << std::endl;
    std::cout << "" << std::endl;
    std::cout << "The area is " << 6 * 10 << std::endl;
    std::cout << "The perimeter is " << 2 * (10 + 6) << std::endl;
}
